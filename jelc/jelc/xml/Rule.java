package jelc.xml;

public class Rule {
	private String shortRule;
	private String longRule;
	
	public String getLongRule() {
		return longRule;
	}
	
	public void setLongRule(String longRule) {
		this.longRule = longRule;
	}
	
	public String getShortRule() {
		return shortRule;
	}
	
	public void setShortRule(String shortRule) {
		this.shortRule = shortRule;
	}
}
